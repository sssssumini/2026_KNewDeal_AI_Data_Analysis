# == 실습 1 : 사칙연산 계산기 ==
print("== 실습 1 : 사칙연산 계산기 ==")
a = 123
b = 23

print(a, "+", b, "=", a + b)
print(a, "-", b, "=", a - b)
print(a, "*", b, "=", a * b)
print(a, "/", b, "=", a / b)
print(a, "//", b, "=", a // b)
print(a, "%", b, "=", a % b)
print(a, "**", b, "=", a**b)

# == 실습 2 : 평균과 도형 계산 ==
print("== 실습 2 : 평균과 도형 계산 ==")
width = 30
length = 20
height = 55
print(
    "평균 =",
    (width + length + height) / 3,
    "넓이 =",
    width * length,
    "부피 =",
    width * length * height,
)
