# + : 더하기
# - : 빼기
# * : 곱하기
# / : 나누기

print(7 + 14)  # 21
print(7 - 14)  # -7 (계산 결과 음수여도 출력가능)
print(7 * 14)  # 98
print(7 / 14)  # 0.5

print(75 + 80 / 2)  # 115 (나누기 먼저 계산)
print((75 + 80) / 2)  # 77.5 (괄호로 감싸서 계산 순서 지정)

print("\n========실습, 합, 차, 평균계산=========")
print("두 측정값의 합:", 103 + 70)
print("온도 변화량: ", 103 - 70)
print("평균 온도: ", (103 + 70) / 2)

print("\n========주석=========")
